class STL10_truncated(data.Dataset):

    def __init__(self, root, dataidxs=None, split='train', transform=None, target_transform=None, download=False):
        self.root = root
        self.dataidxs = dataidxs
        self.split = split
        self.transform = transform
        self.target_transform = target_transform
        self.download = download

        self.data, self.target = self.__build_truncated_dataset__()

    def __build_truncated_dataset__(self):
        stl_dataobj = STL10(self.root, split=self.split, transform=self.transform, target_transform=self.target_transform, download=self.download)

        if self.split == 'train':
            data, target = stl_dataobj.data, np.array(stl_dataobj.labels)
        elif self.split == 'test':
            data, target = stl_dataobj.data, np.array(stl_dataobj.labels)

        if self.dataidxs is not None:
            data = data[self.dataidxs]
            target = target[self.dataidxs]

        return data, target

    def __getitem__(self, index):
        """
        Args:
            index (int): Index

        Returns:
            tuple: (image, target) where target is index of the target class.
        """
        img, target = self.data[index], self.target[index]

        # Convert 3D image to PIL Image and then to tensor
        img = Image.fromarray(img.astype(np.uint8).transpose((1 , 2, 0)))  # Convert to PIL Image
        # img = np.array(img)
        # img = torch.from_numpy(img)

        if self.transform is not None:
            img = self.transform(img)
        if self.target_transform is not None:
            target = self.target_transform(target)

        return img, target

    def __len__(self):
        return len(self.data)